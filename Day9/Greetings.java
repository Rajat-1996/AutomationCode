package Day9;

public class Greetings
{
    //1 No Parameter No value
	    void greeting1()
	{
		System.out.println("Hello...");
	}
	
	//2 No Parameter Returns Value
	
	String greeting2()
	{
		return "Hello... How are You?";
	}
	
	//3 Takes Parameter Returns No Value
	
	    void greeting3(String name)
	{
		System.out.println("Hello..."+name);
	}
	
	//4 Take Parameters Return a value
	
	String greeting4(String name)
	{
		return ("Hello.."+name);
	}
	
	
	
	
	
}
